# Create a module length, which includes the following three functions:

def inch_to_cm(a_inch):
    # inches_to_cm(inches): Convert inches to cm.  1 inch = 2.54 cm.  Return cm.

    cm = 2.54 * a_inch
    return cm


def feet_to_m(a_feet):
    # feet_to_m(feet): Convert feet to m.  1 foot = 0.30 m.  Return m.

    m = 0.30 * a_feet
    return m


def miles_to_km(a_mile):
    # miles_to_km(miles): Convert miles to km.  1 mile = 1.61 km.  Return km

    km = 1.61 * a_mile
    return km
